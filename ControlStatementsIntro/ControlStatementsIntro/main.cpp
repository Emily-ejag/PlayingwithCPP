#include <iostream>

using namespace std;

void main() {

	//sequence
	int age = 15;
	cout << age << endl;

	//decision

	if (age >=16)
	{
		cout << "You can drive" << endl;
	}
	else {
		cout << "You can not drive yet!" << endl;
	}

	//repetition

		//for loop
	for (int i = 1; i < age; i++)
	{
		cout << "Happy " << i << " Birthday" << endl;
	}


}
