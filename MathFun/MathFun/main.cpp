#include <iostream>
#include <cmath>

using namespace std;

int main() {

	cout << "pow(2, 5) = " << pow(2, 5) << endl;
	cout << "sqrt(8) = " << sqrt(8) << endl;
	cout << "ceil(4.2) = " << ceil(4.2) << endl;
	cout << "floor(4.2) = " << floor(4.2) << endl;
	cout << "log2(512) = " << log2(512) << endl;
	return 0;
}